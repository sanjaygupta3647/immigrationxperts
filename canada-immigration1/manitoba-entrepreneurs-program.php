                            <h1 class="section-title__title">Manitoba Entrepreneurs Program</h1>
                            <p class="about-one__text-2">The Manitoba Entrepreneur Pathway is one of the two important
                                streams under the Manitoba Business Investor Stream, which in turn comes under the
                                Manitoba Provincial Nominee Program (MPNP) operated solely by the province of Manitoba.
                                This Manitoba Entrepreneur Pathway was previously recognized as the Manitoba Provincial
                                Nominee Program – Business or the MPNP -B, which is now permanently closed. </p>
                            <p class="about-one__text-2">The Manitoba Business Investor Stream’s Entrepreneur Program
                                provides a pathway for the Canadian Permanent Residents and is designed for investors
                                and entrepreneur who intend to purchase or start a business in Manitoba.</p>
                            <p class="about-one__text-2">The Manitoba Entrepreneur Pathway specifically targets business
                                people who are planning to establish their business across Manitoba, either through
                                purchasing, initiating or by becoming partners with an existing business. These
                                potential candidates arrive in Manitoba on a temporary resident visa (or permit) and are
                                recommended to buy, start and collaborate with a business within a period of 24 months
                                of arriving in Canada, as it is necessary to obtain the permanent resident status. </p>
                            <p class="about-one__text-2">Further, as part of the new changes being introduced in the
                                renewal of the Manitoba Business Investor Stream, all the potential candidates are no
                                longer required to submit $100,000 to the Government of Manitoba to apply for the
                                Manitoba Entrepreneur Program. </p>
                            <p class="about-one__text-2">The two pathways under the Manitoba Business Investor Stream
                                through which investors and entrepreneurs are being targeted are: Manitoba Entrepreneur
                                Pathway and the Farm Investor Pathway. </p>
                            <h3 class="txt mt-4">Manitoba Business Investor Stream: Entrepreneur Pathway</h3>
                            <p class="about-one__text-2">The Manitoba Provincial Nominee Program or the MPNP has
                                introduced an “Interim Process” as part of the “Manitoba Entrepreneur Pathway Soft
                                Launch” which guides the potential candidates to apply for the Business Investor Stream
                                Entrepreneur Pathway through submitting required forms. The Interim Process will remain
                                in place till some prospective changes to the MPNP Online System would be complete.
                                Therefore, once the enhancements in the system would completed, the Interim Process will
                                be removed and then all the future candidates will process their application through the
                                online application system. </p>
                            <h3 class="txt mt-4">Interim Process Overview</h3>
