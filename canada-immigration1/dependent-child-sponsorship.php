                            <p class="about-one__text-2">Some potential sponsors may face obstacles in applying for the
                                Parent and Grandparent Program, either because they didn’t receive an invitation or
                                because they prefer a faster process to bring their elderly relatives to Canada.
                                Fortunately, there is an alternative option available. </p>
                            <p class="about-one__text-2">Through the Super Visa program, parents and grandparents of
                                Canadian citizens or permanent residents can have extended stays in Canada, with the
                                possibility of staying for up to two years upon their first entry. The Super Visa is
                                valid for up to a decade, allowing its holders to return to Canada multiple times during
                                that period.</p>
                            <p class="about-one__text-2">The Super Visa is not a program that offers permanent
                                residence. Those who apply for it must have their medical insurance and are not allowed
                                to work in Canada unless they have specific authorization. Despite these restrictions,
                                the processing times are relatively fast, allowing families to reunite in Canada more
