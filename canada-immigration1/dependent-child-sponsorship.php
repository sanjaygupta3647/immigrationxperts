                                requirements: they must be 18 years old or above, and they must hold either Canadian
                                citizenship or permanent residency status.</p>
                            <p class="about-one__text-2">Sponsors must commit to fulfilling the essential requirements
                                of their intended sponsored person and sign a pledge to offer financial aid to their
                                sponsored family member after they acquire permanent residency. Furthermore, they must
                                reimburse any social assistance payments obtained by the sponsored family member
                                throughout the pledge.</p>
