                                requirements. We will look into the process for each stream under AINP.</p>
                            <h5 class="txt mt-4">Alberta Opportunity Stream</h5>
                            <p class="about-one__text-2">This stream provides a pathway to Canada PR visa for immigrants
                                who are working in Alberta and international graduates who have completed their studies
                                at a recognized Alberta educational post-secondary institution. Eligibility for this
                                stream is based on the following factors:</p>
                            <p class="about-one__text-2"><span>i. Residency Status and Work Permit Requirements</span>
                                Candidate must have:</p>
                            <p class="about-one__text-2"><span>➤</span> Valid temporary status in Canada that authorizes
                                eligibility to work in Canada</p>
                            <p class="about-one__text-2"><span>➤</span> A valid work permit (not implied status or
                                restoration status) for an eligible occupation based on either:</p>
                            <p class="about-one__text-2"><span>A Positive Labour Market Impact Assessment (LMIA)</span>
                            </p>
                            <p class="about-one__text-2"><span>An LMIA exemption for workers</span></p>
                            <p class="about-one__text-2"><span>An LMIA exemption as a Post-Graduation Work Permit (PGWP)
                                    holder</span> </p>
                            <p class="about-one__text-2"><span>ii. Language Proficiency</p>
                            <p class="about-one__text-2">The minimum requirement for language proficiency under this
                                stream is to have Canadian Language Benchmark (CLB) level 4. It can be in either
                                language whether it is in English or French. Candidates have to prove their knowledge of
                                the language in four sections – Reading, Writing, Speaking and Listening.</p>
                            <p class="about-one__text-2"><span>iii. Education requirements</span></p>
                            <p class="about-one__text-2">At the time of applying under this stream, the candidate needs
