                            <h2 class="txt mt-4">Express entry process of Alberta immigration nominee program (AINP)
                            
                            </h2>
                            <p class="about-one__text-2">This stream allows the AINP to nominate skilled and capable
                                immigrants from the federal government’s express entry program. For express entry
                                candidates to apply under AINP, they must be invited by the province to do so. To get
                                invited, the candidate must show strong ties to the province or can be the one that will
                                support the province’s economic growth and diversification priorities. International
                                graduates from Canadian post-secondary institutions can also be asked to submit under
                                this stream.</p>
                            <p class="about-one__text-2"><span>How to get Notification of Interest and get selected from
                                    the express pool for AINP?</span></p>
                            <p class="about-one__text-2">Based on the credentials submitted by the candidate to the
                                AINP, the province can select the candidate and send them a Notification of Interest
                                (NOI). In order to get an NOI letter, the candidate:</p>
                            <p class="about-one__text-2"><span>➤</span> Must have an active express entry profile</p>
                            <p class="about-one__text-2"><span>➤</span> Must show an intention to immigrate to province
                                and settle their permanently</p>
                            <p class="about-one__text-2"><span>➤</span> Must be working in an occupation that supports
                                Alberta’s economic development and diversification</p>
                            <p class="about-one__text-2"><span>➤</span> Must have minimum Comprehensive Ranking System
                                (CRS) score of 300 points</p>
                            <p class="about-one__text-2">In case the candidate is able to follow one or more of the
                                following adaptability factors, it will increase the likelihood of getting an NOI:</p>
                            <p class="about-one__text-2"><span>➤</span> Alberta job offer and/or work experience</p>
                            <p class="about-one__text-2"><span>➤</span> A graduate of a Canadian post-secondary
                                institution</p>
                            <p class="about-one__text-2"><span>➤</span> A relative that is living in the province:
                                parent, child, brother and/or sister</p>
                            <p class="about-one__text-2">Once the candidate’s application is approved, they will receive
                                a nomination certificate via the IRCC Express entry portal. Candidate must accept the
                                nomination within 30 days after it is issued in order to receive an Invitation to Apply
                                (ITA).</p>
                            <p class="about-one__text-2">After getting an ITA, the candidate has 60 days to submit their
                                application for a permanent residence visa to the IRCC. </p>
