                                shortage of capable individuals. To fill this gap of skilled labor, the provincial
                                government of Alberta has a special agreement with the Immigration, Refugees &
                                Citizenship Canada (IRCC) that allows them to nominate the skilled and capable
                                immigrants for permanent residency in the province. This immigration pathway is known as
                                the Alberta Immigrant Nominee Program (AINP).</p>
