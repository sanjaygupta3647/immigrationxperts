                            <p class="about-one__text-2">With the Alberta Immigrant Nominee Program (AINP), the province
                                is able to target immigrants that are in high demand throughout the province and the
                                fill the shortage of skilled labor with immigrants that have the training and experience
                                needed. This program allows prospective immigrants with the right mix of experience and
                                skills to get an Alberta Provincial Nomination Certificate.</p>
                            <p class="about-one__text-2">Being nominated and by applying via the Alberta Immigrant
                                Nominee Program (Alberta PNP program 2020), will speed up the permanent residency
                                application process by some time.</p>
