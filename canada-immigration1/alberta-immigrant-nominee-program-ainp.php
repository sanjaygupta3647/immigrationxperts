                            <p class="about-one__text-2">Skilled and semi-skilled immigrants can apply for Alberta PNP
                                2020 and there are also options for immigrants to apply with or without any employer
                                support. Alberta PNP is mainly broken into three main immigration streams:</p>
                            <p class="about-one__text-2"><span>➤ Alberta Opportunity Stream :</span> This stream is for
