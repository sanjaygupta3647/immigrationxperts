                                the region. The foreign education needs to be verified through an Educational Credential
                                Assessment (ECA).</p>
                            <p class="about-one__text-2"><span>iv. Work Experience</span>
                                Candidate must have:</p>
                            <p class="about-one__text-2"><span>➤</span> Have a minimum of 12 months of work experience
                                for a “current occupation” in Alberta within the last 18 months.</p>
                            <p class="about-one__text-2"><span>➤</span> Have a minimum of 24 months of work experience
                                in “current occupation” in Canada and/or outside of Canada within the last 30 months.
