                            <h1 class="section-title__title">Manitoba Provincial Nominee Program</h1>
                            <p class="about-one__text-2">The Manitoba Provincial Nominee Program or the MPNP is one of
                                the leading Provincial Nominee Programs(PNPs) of the Canadian Government. This
                                immigration program enables the province of Manitoba to nominate eligible individuals
                                and families for immigration to Canada after evaluating their profile based on certain
                                eligibility criteria being set by the province.</p>
                            <p class="about-one__text-2"><span>Manitoba Immigration</span> </p>
                            <p class="about-one__text-2">Since its first launch in 1998, approximately 130,000
                                immigrants have arrived in Canada through the MPNP only within the first two decades of
                                the launch of the program. In its current scenario, the Manitoba Immigration Authorities
                                are doing plausible efforts to welcome more qualified immigrants in the coming years.
                            </p>
                            <p class="about-one__text-2">The MPNP however underwent an important overhaul in the year
                                2019, a process that remain continued for 2020. Changes include introduction of some new
                                immigration pathways and streams, which includes incorporation of a dedicated
                                immigration stream for Express Entry candidates that have concrete plans to settle in
                                Manitoba for an extended period of time.</p>
                            <p class="about-one__text-2">Some other new features included in the MPNP includes a pathway
                                designed specifically for providing faster application approval for international
                                applicants that have completed their graduation from an academic institute based in
                                Manitoba, and also another new stream designed to attract business immigrants.</p>
                            <p class="about-one__text-2">Majority of these new immigration pathways to Manitoba
                                Provincial Nominee Program (MPNP)will be kept under the existing International Skilled
                                Worker Stream, which will operate in the same manner until some new changes are added in
                                this immigration category. The current criteria for the MPNP being followed under this
                                stream, which is divided into two sub-streams, that are the Skilled Worker Overseas and
                                Skilled Workers in Manitoba categories, would remain active for filing the Expression of
                                Interest (EOI) and also for the receiving new applications from the province.</p>
                            <p class="about-one__text-2">There are total four streams under the updates Manitoba
                                Provincial Nominee Program (MPNP), which further includes within them some more
                                immigration pathways.</p>
                            <p class="about-one__text-2"><span>Skilled Worker Overseas Stream</span> The MPNP designed
                                for Skilled Workers is a locally driven immigration stream and is based on the needs and
                                requirements of the Manitoba employers. The provincial government of Manitoba selects
                                experienced as well as internationally trained workers who possess the required skilled
                                that are in-demand within the local labour market of Manitoba, and then nominate them
                                for the permanent resident visa, thus allowing them to live and work in Manitoba. The
                                MPNP which specifically targets skilled individuals through its Skilled Worker Overseas
                                Stream (SWO) gives priority and nominates candidates who successfully demonstrate a
                                prior connection with the province via:</p>
                            <p class="about-one__text-2"><span>➤</span> the support of family members and friends;</p>
                            <p class="about-one__text-2"><span>➤</span> through prior work experience or education in
                                the province; or</p>
                            <p class="about-one__text-2"><span>➤</span> through an invitation to apply (ITA) bring
                                received directly from the MPNP as part of their Strategic Recruitment Initiative.</p>
                            <h5 class="txt mt-4">Step-by-Step process to apply for the Skilled Worker Overseas Stream
                            </h5>
                            <p class="about-one__text-2"><span>Immigration to Manitoba through the MPNP stream designed
                                    to target and bring Skilled Workers to Manitoba is a complete three-step process,
                                    described as follows:</span> </p>
                            <p class="about-one__text-2"><span>Step 1:</span> Submit an Expression of Interest (EOI)</p>
                            <p class="about-one__text-2"><span>Step 2:</span> The highest-scoring eligible applicants
                                with a prior connection to Manitoba will be invited to submit an application towards the
                                MPNP</p>
                            <p class="about-one__text-2"><span>Step 3:</span> Following a complete and in-depth
                                assessment, the MPNP nominated qualified candidates, which would then be required to
                                submit a separate application towards the Government of Canada to apply for the
                                permanent resident visa for you (primary/main applicant) and your family (dependents)
                            </p>
                            <p class="about-one__text-2">The Skilled Worker Overseas Stream (SWO)comprises of Canada
                                Express Entry Immigration Pathway and the other direct provincial pathway.</p>
                            <p class="about-one__text-2">The SWO is aimed towards inviting international skilled workers
                                in Manitoba that have relevant skills and training in the in-demand occupations of
                                Manitoba. However, along with this, priority would be given to those applicants and
                                their dependents that have close family connections in the province, eligible language
                                proficiency and previous training and work experience needed to find an employment
                                quickly after arriving in the province.</p>
