                                level and work experience gained in Canada is worth 10 points for each of the partner.
                                This makes a total of 40 points that can be claimed under your spouse or partner’s
                                factors. </p>
                            <p class="about-one__text-2">However, even if you apply as a single applicant, then these 40
                                points can still be distributed across various other factors in our own profile. Single
                                applicants have to simply claim these points under their own abilities. </p>
