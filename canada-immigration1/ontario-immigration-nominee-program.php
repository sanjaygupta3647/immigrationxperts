                            <p class="about-one__text-2">As of May 2016 there are certain high volume OINP streams that
                                have been put on temporary hold for taking up applications to select them for permanent
                                residency, as announced by Ontario. However, lower volume streams are still open for
                                prospective candidates and are accepting applications for the Canada Immigration
                                Permanent Residency.</p>
                            <h5 class="txt mt-4">Ontario Express Entry Category</h5>
                            <p class="about-one__text-2">Under this category there are two streams for requirements are
                                mentioned below:</p>
                            <h5 class="txt mt-4">Human Capital Priorities Stream</h5>
                            <p class="about-one__text-2">As of 16th February 2016, NOI, as Notification of Interest,
                                under the Human Capita Priorities Stream has been put on temporary hold. With this
                                stream in hand the prospective candidates can get nominated by OINP to enter Federal’s
                                Express Entry pool of eligible candidates provided they are equipped with needed
                                education qualification, language skills, work experience and similar attributes that
                                will aid them to gain a spot in the Ontario’s communities and labour market with full
                                success and smooth integration.</p>
                            <p class="about-one__text-2"><span>Eligibility Requirement under OINP:</span> </p>
                            <p class="about-one__text-2"><span>➤ </span> The CRS score should be at least 400. The
                                applicant must ensure that while processing under Ontario Nomination as well as at the
                                stage of Federal application for permanent residency the CRS score should not go below
                                400. It should remain at 400 or go beyond.</p>
                            <p class="about-one__text-2"><span>➤ </span> The applicant should be ready with either a
                                Bachelor’s, PhD or Master’s from a Canadian education institution or have an ECA
                                (Education Credential Assessment) report generated from a recognized institution
                                providing proof that the foreign education level is equivalent with that of Canadian
                                Master’s, PhD or Bachelor’s degree.</p>
                            <p class="about-one__text-2"><span>➤ </span> At least some level of work experience:</p>
                            <p class="about-one__text-2"><span>➤ </span> The applicant should show minimum proficiency
                                level under CLB (Canadian Language Benchmark) level 7or beyond for all four abilities
                                that are, speaking, writing, listening and reading. This ability and CLB7 must be
                                provided with proof in the form of test results from any of the following:</p>
                            <p class="about-one__text-2"><span>➤ </span> The applicant should show intention to stay in
                                Ontario, by providing proof in form of intent and indication of ties to Ontario
                                statement.</p>
                            <p class="about-one__text-2"><span>➤ </span> The prospective candidate should have adequate
                                personal funds to cover the cost of living in Ontario which should be backed by bank
                                statements.</p>
                            <p class="about-one__text-2"><span>➤ </span> Additional requirements set under CEC and FSWP
                                category should be met fully.</p>
                            <h5 class="txt mt-4">French-Speaking Skilled Worker Stream</h5>
                            <p class="about-one__text-2">This stream under Express Entry Category is for those
                                prospective candidates who are skilled workers having some level of proficiency in
                                French language along with hold on language English as well wishing to reside and work
                                on permanent basis in Ontario. Under this stream OINP gets to put forth those applicants
                                in the pool of candidates of Express Entry program provided they are equipped with
                                needed education qualification, language skills, work experience and similar attributes
                                that will aid them to gain a spot in the Ontario’s communities and labour market with
                                full success and smooth integration.</p>
                            <p class="about-one__text-2"><span>Eligibility Requirements:</span> </p>
                            <p class="about-one__text-2"><span>➤ </span> The applicant should show minimum proficiency
                                under CLB (Canadian Language Benchmark) level 7 for French and CLB level 6 for English
                                for all four abilities that are, speaking, writing, listening and reading. This ability
                                and CLB7 / CLB6 must be provided with proof in the form of test results from any of the
                                following:</p>
                            <p class="about-one__text-2"><span>➤ </span> The applicant should be ready with either a
                                Bachelor’s, PhD or Master’s from a Canadian education institution or have an ECA
                                (Education Credential Assessment) report generated from a recognized institution
                                providing proof that the foreign education level is equivalent with that of Canadian
                                Master’s, PhD or Bachelor’s degree.</p>
                            <p class="about-one__text-2"><span>➤ </span> The applicant should show intention to stay in
                                Ontario, by providing proof in form of intent and indication of ties to Ontario
                                statement.</p>
                            <p class="about-one__text-2"><span>➤ </span> The prospective candidate should have adequate
                                personal funds to cover the cost of living in Ontario which should be backed by bank
                                statements.</p>
                            <h5 class="txt mt-4">General Category</h5>
                            <p class="about-one__text-2">From 9th May, 2016 onwards the applications for Pre-screening
                                of employer process has been put on hold for momentary period. However those Foreign
                                Skilled Workers with job offers applicants who have already received approval from OINP
                                can continue to file for Nominee process and submit the required application.</p>
                            <p class="about-one__text-2">Under this category, employers of Ontario recruit those foreign
                                workers who are eligible, approved for some specific skilled positions with NOC skill
                                type 0 or with skill level A or B as classified by the Government of Canada. These
                                prospective candidates could be staying in Canada or abroad while filing for the
                                application.</p>
                            <h5 class="txt mt-4">International Student Category</h5>
                            <p class="about-one__text-2">From 9th May 2016 onwards, Master’s and PhD graduate stream has
                                put hold on accepting applications from new prospective candidates. However, the stream
                                for International Student with Job Offer is still open and can be applied by prospective
                                candidates who already have a job opportunity which has been given all clear after
                                thorough verification via Employer Pre-screen process. This category under OINP for
                                Canada Permanent Residency is created for students of foreign nationals. There are three
                                basic streams under this category.</p>
                            <p class="about-one__text-2"><span>International Student with a Job Offer Stream</span> </p>
                            <p class="about-one__text-2">This streams aims to assist Canadian employers to enroll
                                students of foreign nationals’ compatible of job opening present with them. It is open
                                for employers in Ontario and students with job opportunity in the Ontario province.</p>
                            <p class="about-one__text-2"><span>Minimum Requirements need to be met by Foreign National
                                    students with an approved job opportunity from a Canadian employer to become
                                    eligible are:</span> </p>
                            <p class="about-one__text-2"><span>➤ </span> Have been established for at least 36 months at
                                the time of application;</p>
                            <p class="about-one__text-2"><span>➤ </span> Currently employ key staff seeking nominations,
