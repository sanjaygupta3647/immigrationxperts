                            <h1 class="section-title__title">Nova Scotia</h1>
                            <p class="about-one__text-2">Nova Scotia is a small eastern Canada’s Maritime province of
                                Atlantic and is one of the provinces of Atlantic Canada. Nova Scotia’s economy is
                                basically resource-based and has a booming economy. To sustain the economic development
                                of the province, Nova Scotia is in need of skilled individuals who will be able to meet
                                the demands of the local labor market.</p>
                            <p class="about-one__text-2">To meet the demands of the labour market, Nova Scotia has an
                                immigration pathway for the skilled immigrants who will be able to help in the economic
                                development of the province and that immigration pathway is known as Nova Scotia
                                Provincial Nomination Program (NS PNP).</p>
                            <h3 class="txt mt-4">What is Nova Scotia PNP Program?</h3>
                            <p class="about-one__text-2">Nova Scotia’ provincial nomination program - Nova Scotia
                                Provincial Nominee Program (NS PNP) allows the province to nominate the prospective
                                immigrants for permanent resident status in the province with skill-sets and work
                                experience that is targeted by the Nova Scotia’s local labor market.</p>
                            <h5 class="txt mt-4">What are the different stream under Nova Scotia PNP process 2019?</h5>
                            <p class="about-one__text-2"><span>Nova Scotia Provincial Nominee Program accepts
                                    applications for the following immigration streams:</span> </p>
                            <p class="about-one__text-2"><span>➤</span> Nova Scotia Demand: Express Entry</p>
                            <p class="about-one__text-2"><span>➤</span> Nova Scotia Experience: Express Entry</p>
                            <p class="about-one__text-2"><span>➤</span> Nova Scotia Labour Market Priorities</p>
                            <p class="about-one__text-2"><span>➤</span> Skilled Worker Stream</p>
                            <p class="about-one__text-2"><span>➤</span> Entrepreneur Stream</p>
                            <p class="about-one__text-2"><span>➤</span> International Graduate Entrepreneur Stream</p>
                            <p class="about-one__text-2"><span>➤</span> Physician Stream </p>
                            <h5 class="txt mt-4">1) Nova Scotia Demand: Express Entry</h5>
                            <p class="about-one__text-2">This stream uses the federal government’s express entry program
                                selection system to select the candidates for provincial nomination. Highly skilled
                                immigrants are selected who wants to live in the province as a permanent resident. There
                                are two categories under this stream:</p>
                            <p class="about-one__text-2"><span>➤ </span> Category A – for applicants with a job offer
                            </p>
                            <p class="about-one__text-2"><span>➤ </span> Category B – Applicants with experience in
                                opportunity occupation. Category has been closed by NSNP</p>
                            <p class="about-one__text-2"><span>To apply Category A, following are the eligibility
                                    criteria:</span> </p>
                            <p class="about-one__text-2"><span>➤ </span> Must have profile registered in Express entry
                                program</p>
                            <p class="about-one__text-2"><span>➤ </span> Score minimum of 67 points on the stream’s six
                                selection factors</p>
                            <p class="about-one__text-2"><span>➤ </span> Must have a job offer from an eligible employer
                                of province for a full-time skilled work that lasts at least one year after candidate’s
                                permanent resident visa is issued</p>
                            <p class="about-one__text-2"><span>➤ </span> Must have 1 year of skilled work experience
                                related to the job</p>
                            <p class="about-one__text-2"><span>➤ </span> For educational qualification, must have a
                                Canadian high school credential or equivalent</p>
                            <p class="about-one__text-2"><span>➤ </span> For Language proficiency, minimum Canadian
                                Language Benchmark (CLB) level 7</p>
                            <p class="about-one__text-2"><span>➤ </span> Must possess enough financial resources to
                                settle in the province successfully</p>
                            <h5 class="txt mt-4">2) Nova Scotia Experience: Express Entry</h5>
                            <p class="about-one__text-2">Province uses this stream to select candidates for this stream
                                from federal government’s express entry program, to provide an immigration pathway to
                                permanent resident for skilled immigrants who have worked for a province’s employer for
                                a minimum of one year. In order to apply candidate must:</p>
                            <p class="about-one__text-2"><span>➤ </span> Have at least 1 year of work experience in Nova
                                Scotia</p>
                            <p class="about-one__text-2"><span>➤ </span> Age group must be between 21 to 55 years old
                            </p>
                            <p class="about-one__text-2"><span>➤ </span> Have a Canadian high school credential or
                                equivalent</p>
                            <p class="about-one__text-2"><span>➤ </span> Prove language ability in English or French on
                                CLB level 7 minimum for NOC skill level 0 and A. For NOC skill level B, CLB 4 is the
                                minimum requirement</p>
                            <p class="about-one__text-2"><span>➤ </span> Have profile registered with IRCC’s express
                                entry system</p>
                            <h5 class="txt mt-4">3) Nova Scotia Labour Market Priorities</h5>
                            <p class="about-one__text-2">Via this stream, NS PNP is able to nominate the skilled express
                                entry candidates who are able to meet the labour market needs. To be eligible to apply,
                                candidates must:</p>
                            <p class="about-one__text-2"><span>➤ </span> Receive a Letter of Interest (LOI) from NSNP
                                within the express entry system</p>
                            <p class="about-one__text-2"><span>➤ </span> Submit application within 30 calendar days on
                                which the LOI was issued</p>
                            <p class="about-one__text-2"><span>➤ </span> Meet the minimum work experience experiment
                                requirement for express entry stream</p>
                            <p class="about-one__text-2"><span>➤ </span> Demonstrate sufficient funds to successfully
                                establish themselves and accompanying family members and pay immigration costs and
                                travel expenses</p>
                            <p class="about-one__text-2"><span>➤ </span> Have legal status in current country of
                                residence</p>
                            <p class="about-one__text-2"><span>➤ </span> Meet eligibility criteria in place at the time
                                of LOI issuance</p>
                            <h5 class="txt mt-4">4) Skilled Worker Stream</h5>
                            <p class="about-one__text-2">NSNP via this stream nominates the skilled immigrants who have
                                received a job offer from an eligible province employer. Immigrants in skilled,
                                semi-skilled, and low-skilled occupations may be eligible to apply. To apply candidate
                                must:</p>
                            <p class="about-one__text-2"><span>➤ </span> Have a full-time permanent job offer from a
                                province employer</p>
                            <p class="about-one__text-2"><span>➤ </span> Have 1 year of work experience that is related
                                to the job</p>
                            <p class="about-one__text-2"><span>➤ </span> Age group must be between 21 to 55 years old
                            </p>
                            <p class="about-one__text-2"><span>➤ </span> Have a high school diploma</p>
                            <p class="about-one__text-2"><span>➤ </span> Have the appropriate training, skills and/or
                                accreditation for the job</p>
                            <p class="about-one__text-2"><span>➤ </span> For a skilled worker, minimum CLB level 5 is
                                required. For semi-skilled and low-skilled, minimum CLB level 4 is required.</p>
                            <p class="about-one__text-2"><span>➤ </span> Show enough financial resources to successfully
                                settle in the province of Nova Scotia</p>
                            <h5 class="txt mt-4">5) Entrepreneur Stream</h5>
                            <p class="about-one__text-2">This stream is to attract immigrants who want to buy or invest
                                in a business and settle permanently in Nova Scotia. Following are the eligibility
                                criteria, for candidates to fulfill:</p>
                            <p class="about-one__text-2"><span>➤ </span> Must be minimum 21 years of age or older</p>
                            <p class="about-one__text-2"><span>➤ </span> Must have intention to live permanently in the
                                province while owning and actively managing a Nova Scotia business</p>
                            <p class="about-one__text-2"><span>➤ </span> Have a net worth of at least $600,000 CAD</p>
                            <p class="about-one__text-2"><span>➤ </span> Be able to make a personal investment of
                                $150,000 CAD to establish a business in the province</p>
                            <p class="about-one__text-2"><span>➤ </span> Must have a minimum of 3 years of work
                                experience for actively managing and owning a business (1/3rd ownership minimum) OR more
                                than 5 years of work experience in a senior business management role</p>
                            <p class="about-one__text-2"><span>➤ </span> Have a score of at least 5 on CLB in speaking,
                                listening, reading and writing in either English or French</p>
                            <p class="about-one__text-2"><span>➤ </span> Complete an online Expression of Interest (EOI)
                            </p>
                            <p class="about-one__text-2"><span>➤ </span> Receive an Invitation to Apply (ITA) to apply
                                from Nova Scotia Office of Immigration </p>
                            <h5 class="txt mt-4">6) International Graduate Entrepreneur Stream</h5>
                            <p class="about-one__text-2">This stream is open for graduates who have completed a minimum
                                of two years of full-time study from a recognized Nova Scotia university or Nova Scotia
                                Community College, have operated their own business in Nova Scotia for at least one
                                year, and intend to settle permanently via Nova Scotia process . To apply candidate
                                must:</p>
                            <p class="about-one__text-2"><span>➤ </span> Want to live permanently in province while
                                owning and managing a provincial business</p>
                            <p class="about-one__text-2"><span>➤ </span> Have a minimum of one year’s continuous
                                experience actively managing and owning current business (33.3% ownership minimum)</p>
                            <p class="about-one__text-2"><span>➤ </span> Have completed a degree or diploma after at
                                least 2 full-time academic year, in person study at a university in the province of Nova
                                Scotia community college</p>
                            <p class="about-one__text-2"><span>➤ </span> Have a valid post-graduation work permit</p>
                            <p class="about-one__text-2"><span>➤ </span> Must have minimum CLB level 7 in speaking,
                                reading, writing and listening in either English or French</p>
                            <p class="about-one__text-2"><span>➤ </span> Completed an online Expression of Interest</p>
                            <p class="about-one__text-2"><span>➤ </span> Receive an Invitation to Apply (ITA) from Nova
                                Scotia Office of Immigration</p>
                            <h5 class="txt mt-4">7) Physician Stream</h5>
                            <p class="about-one__text-2">This stream is open to internationally-trained general
                                practitioners, family physicians and specialist physicians with a signed employment
                                offer from Nova Scotia’s healthcare authorities. To apply candidate must:</p>
                            <p class="about-one__text-2"><span>➤ </span> Want to live in the province permanently while
                                owning and managing a Nova Scotia business</p>
                            <p class="about-one__text-2"><span>➤ </span> Have minimum one year’s continuous experience
                                actively managing and owning current provincial business (33.3% ownership minimum)</p>
                            <p class="about-one__text-2"><span>➤ </span> Have completed a degree or diploma after
                                minimum 2 academic year of full-time, in-person study at Nova Scotia university or Nova
                                Scotia Community College</p>
                            <p class="about-one__text-2"><span>➤ </span> Have a valid post-graduation work permit</p>
                            <p class="about-one__text-2"><span>➤ </span> Must have minimum CLB level 7 in reading,
                                listening, speaking and writing in either English or French</p>
                            <p class="about-one__text-2"><span>➤ </span> Receive an ITA from Nova Scotia Office of
                                Immigration</p>
                            <h5 class="txt mt-4">What are Nova Scotia PNP program requirements?</h5>
                            <p class="about-one__text-2">As we have discussed above, every streams/categories of Nova
                                Scotia Provincial Nominee Program (NS PNP) has their own requirements that needs to
                                fulfill in order to get nominated by the Nova Scotia’s Immigration Office. We have
                                generalized the requirements that are occurring theme for all the streams. These
                                requirements are as follows:</p>
                            <p class="about-one__text-2"><span>➤ </span> Candidate’s field of work must be mentioned in
                                occupation in-demand list of the province</p>
                            <p class="about-one__text-2"><span>➤ </span> Age group must be between 18 to 49 years</p>
                            <p class="about-one__text-2"><span>➤ </span> Must be able to attain a minimum 4.5 in
                                International English Language Testing System (IELTS) or CLB level 4 on language ability
                            </p>
                            <p class="about-one__text-2"><span>➤ </span> Must have minimum 1 year of work experience in
                                a relevant occupational field</p>
                            <p class="about-one__text-2"><span>➤ </span> Must possess an Educational Credential
                                Assessment (ECA) for qualification diploma/certificate to Canadian education system</p>
                            <p class="about-one__text-2"><span>➤ </span> Must be able to show an intention to live in
                                Nova Scotia</p>
