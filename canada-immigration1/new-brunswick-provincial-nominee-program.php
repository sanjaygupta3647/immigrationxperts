					<h1>New Brunswick Provincial Nominee Program</h1>
					
					   <p>New Brunswick is one of three provinces collectively known as the “Maritimes.”If one were to callously define the countryside of New Brunswick, one would describe it as largely being covered of trees. About 30 percent of the population speak French as the first language making it the only official bilingual province in Canada. New Brunswick homes major cities like Moncton, Dieppe, Fredericton, Saint John, Edmundston etc. that keeps you to the happening nightlife and unspoiled nature.</p>

                        <p>NBPNP refers to the New Brunswick’s Provincial Nominee Program. This program is a bridge for the prospective immigrants who hold fine set of skills and experience to enter the province and fetch the New Brunswick Provincial Nomination Certificate. The certificate gives the authority to apply for Canadian permanent residence with more agile processing compared to other immigration classes of Canada. The NBPNP welcomes applications for the following immigration categories:</p>
                        <ul class="list-immigration">
                         	<li><strong>Express Entry Labour Market Stream</strong></li>
                         	<li><strong>Skilled Workers with Family Support</strong></li>
                         	<li><strong>Skilled Workers with Employer Support</strong></li>
                         	<li><strong>Business Applicants</strong></li>
                        </ul>
                            <strong>Express Entry Labour Market Stream</strong>
                            
                            <p>The New Brunswick Provincial Nominee Program (NBPNP) has announced its Express Entry Labour Market Stream. According to this stream the NBPNP can follow the Federal&nbsp; Express Entry immigration &nbsp;selection system to meet New Brunswick’s specific labour market and its statistical requirements. This stream functions under an ‘Expression of Interest’ (EOI) process.  </p>
                            					    
                            					    
                            <p>The prospective immigrants have to complete an EOI form and send it to the NBPNP. The forms are received from 1st to 15th of each month. The candidates are authorized a score based on the information provided in the EOI. Further, they are evaluated on the basis of the following criteria:</p>
                                <ul class="list-immigration">
                                 	<li>Valid proof of training and experience as authorized by the province;</li>
                                 	<li>Highest score after assessment;</li>
                                 	<li>Determine the capability to become economically stable in the province; and</li>
                                 	<li>Possibility of contribution to the provincial labour market.</li>
                                </ul>
                                <p>The candidates with highest scores are invited to apply to the NBPNP via email. If the candidates are further nominated, it will be processed through the federal government’s Express Entry electronic immigration management system, which assures entire application processing within six months of submission.</p>
                                
                                <p>The NBPNP follows standardized acceptance norm of expression of interest (EOI) forms from individuals who determine the following criteria:</p>
                                
                                <p>1. A connection to New Brunswick, OR</p>
                                
                                <p>2. Has attended a NBPNP information session in person in the past two years, OR</p>
                                
                                <p>3. Open Category</p>	
                                
                                
                                
                                <p><strong>EELMS Categories</strong></p>

								<p>Below are the main categories for NBPNP.</p>

								<p>Make a note that for either of these categories highly skilled French speakers is the first preference for New Brunswick. The province encourages people who can demonstrate advanced French language ability through a Test d’évaluation de français (TEF) and are desirous to be a part of the Francophone society of New Brunswick will be given utmost priority.</p>

								<p><strong>1. Connection to New Brunswick</strong></p>

								EOI forms will be accepted by NBPNP from those candidates who are able to showcase minimum one connection to New Brunswick. But priority will be given to those candidates who:
								<ul class="list-immigration">
									<li>Having relatives holding Canadian PR or are Canadian citizens for minimum one year in New Brunswick</li>
									<li>Full time work experience in New Brunswick for at least one year in the past five years;</li>
									<li>Hold post-secondary education diplomas from any recognized college or university of New Brunswick;</li>
									<li>Employed in New Brunswick;</li>
									<li>Have a permanent full time offer of employment from a New Brunswick company; or</li>
									<li>Hold French as their first language backed by their Test d’évaluation de français (TEF) certificate.</li>
								</ul>
								<p><strong>NOTE:</strong>&nbsp;First priority will be given to those applicants who determine a valid connection to New Brunswick and also have a work experience in a priority occupation.</p>

								<p><strong>2. Attended a NBPNP information session</strong></p>

								<p>EOI forms are accepted by NBPNP in the weekdays from the candidates who have witnessed NBPNP information session or have met with NBPNP staff at a domestic or international recruitment event in the past two years. The candidate must be able to prove that he had been the part of the information session (like registration date of the event or the name of staff member of NBPNP encountered etc.)</p>

								<p>NOTE: First preference will be given to the candidates having work experience of a occupation in demand mentioned in the ‘Connection to New Brunswick’ section.</p>

								<p><strong>3. Open Category</strong></p>

								<p></p>Note: The NBPNP has stopped accepting Open Category EOIs as of July 15, 2016.<p></p>

								<p>The Open Category specifically issues invitations to apply based on the IT sector. In this category stream a job offer is not mandatory.</p>

								<p>New Brunswick is looking forward for the fresh candidates to come and settle through the Open Category:</p>

								<p><strong>A) Information Systems Analysts And Consultants (NOC 2171)</strong></p>

								<p><strong>Comprise of Titles:</strong>&nbsp;IT Consultant, Computer Systems Analyst, Informatics Consultant, Informatics Security Analyst, Information Systems Business Analyst, Information Systems Quality Assurance Analyst, Management Information Systems (MIS) Analyst, Systems Auditor, Systems Consultant, Systems Security Analyst</p>

								<p><strong>B) Database Analysts And Data Administrators (NOC 2172)</strong></p>

								<p>Comprise of Titles: Data Administrator, Data Custodian, Data Dictionary Administrator, Data Warehouse Analyst, Database Administrator (DBA), Database Analyst, Database Architect, Technical Architect – Database</p>

								<p><strong>C) Computer Programmers And Interactive Media Developers (NOC 2174)</strong></p>

								<p><strong>Comprise of Titles:</strong>&nbsp;Web Programmer, Application Programmer, Business, Application Programmer, Computer Game Developer, Computer Programmer, E-Business (Electronic Business) Software Developer, Interactive Media Developer, Multimedia Developer, Operating Systems Programmer, Programmer Analyst, Scientific Programmer, Software Developer, Software Programmer, Systems Programmer</p>

								<p><strong>D) Web Designers And Developers (NOC 2175)</strong></p>

								<p><strong>Comprise of Titles:</strong>&nbsp;Internet Site Designer, Internet Site Developer, Intranet Site Designer, Web Designer, Web Developer, Web Manager, Web Site Developer, Webmaster, E-Business Web Site Developer</p>

								<p><strong>E) Computer Network Technicians (NOC 2281)</strong></p>

								<p><strong>Comprise of Titles:</strong>&nbsp;Internet Web Site Technician, LAN (Local Area Network) Administrator, LAN Technician, Web Technician, Computer Network Technician, Data Centre Operator, Network Administrator, Network Support Technician, Computer Network Technician Supervisor, System Administrator</p>

								<p>Candidates with work experience in the above mentioned occupations can submit EOI for immigrating to New Brunswick via NBPNP Open Category. A candidate is assigned 600 points under federal Comprehensive Ranking System if a he is successfully invited to apply and obtains a provincial nomination certificate from the NBPNP through this open category.</p>
                                
				
				
				
					<strong>Skilled Workers with Family Support</strong>
						
						In this category the people who have work experience of New Brunswick and have close family ties to the province are aimed.
						
						As per this category the skilled worker applicant or his/her spouse, mandatorily sponsored by a family member in New Brunswick. For the eligibility the applicant must be related to the family supporter in one of the below-mentioned ways:
						<ul class="list-immigration">
							<li>Grandchild</li>
							<li>Non-dependent child</li>
							<li>Brother or sister</li>
							<li>Niece or nephew (children of brothers or sisters)</li>
						</ul>
						<strong>Minimum Requirements for Applicants</strong>
						
						The eligibility criteria for the candidates for the Skilled Worker with Family Support stream are as follows:
						<ul class="list-immigration">
							<li>Age between 22 and 50 years;</li>
							<li>Ample ability of English/French to settle in New Brunswick also effectuates the job responsibilities for the desired occupation. The language eligibility test should be done from a reputed agency and the results should not be more than two years old;</li>
						</ul>
						<ul class="list-immigration">
							<li>Should have educational qualifications and/or licence/certification that are essentially required to carry out the job responsibilities of the desired occupation;</li>
							<li>Full time work experience continued up to two years in their desired occupation in New Brunswick, within the last five years;</li>
							<li>Hold a job that is illustrated in one of the following National Occupation Classification (NOC) skill levels:
						<ul class="list-immigration">
							<li>Skill levels 0, A or B</li>
							<li>Skill level C, Category 1, 3, 7, 8, or 9</li>
							<li>Skill level D, Category 1, 3, 7, 8, or 9</li>
						</ul>
						</li>
							<li>Show the willingness to work and settle in New Brunswick and submit a Settlement Plan approved by an immigration Program Officer; and</li>
							<li>Have the required funds to settle in New Brunswick (minimum of $10,000 CAD, and extra $2,000 for each accompanying family member are required.)</li>
						</ul>
						<strong>Minimum Requirements for Family Supporters</strong>
						
						The eligibility criteria for the Family Supporters for the Skilled Worker with Family Support stream are as follows:
						<ul class="list-immigration">
							<li>Should be a Canadian citizen or Canadian permanent resident, and reside full-time in New Brunswick;</li>
							<li>Involved in running a business in New Brunswick for minimum 12 months in sequence at the date of receipt of the NBPNP application; or</li>
							<li>Working Experience in New Brunswick for minimum 12 months at the date of receipt of the NBPNP application;</li>
							<li>The job must be represented in one of the following NOC skill levels:
						<ul class="list-immigration">
							<li>
						<ul class="list-immigration">
							<li>Skill levels 0, A, or B</li>
							<li>Skill level C, Category 1, 3, 7, 8, or 9</li>
						</ul>
						</li>
						</ul>
						</li>
						</ul>
						<ul class="list-immigration">
							<li>Be financially self-supporting;</li>
							<li>Participate in an interview with an official from the Population Growth Division;</li>
							<li>Assist the applicant with a settlement plan; and</li>
							<li>Support only one applicant at a time.</li>
						</ul>
						
						
						
						<strong>Skilled Workers with Employer Support</strong>
						
						<p>Under the&nbsp; Skilled Workers &nbsp;with Employer Support category immigration for workers is facilitated who have received a full-time permanent job offer of employment from a New Brunswick employer.</p>
						
						<strong>Minimum Requirements for Applicants</strong>
						
						The candidates must:
						<ul class="list-immigration">
							<li>Age between 22 and 55 years;</li>
							<li>Proficiency over English or French language(basic level);</li>
							<li>Hold educational qualifications, and/or licenses and certification required for the job in New Brunswick;</li>
							<li>Get an authentic full-time permanent employment offer of employment from an eligible New Brunswick employer;</li>
							<li>Show willingness to work and settle in New Brunswick; and</li>
							<li>Fetch minimum of 50 points on a selection format.</li>
						</ul>
						<strong>Minimum Requirements for Employers</strong>
						
						The employers must be able to prove that:
						<ul class="list-immigration">
							<li>Failed to recruit Canadian citizen or permanent resident for the position;</li>
							<li>The job offers commensurate industry pay rate.</li>
						</ul>
						<strong>Business Applicants</strong>
						
						New Brunswick accepts applications under this category from individuals who plan to own and actively manage a business in New Brunswick. Applicants are expected to make a monetary investment in the business.
						
						<strong>Minimum Requirements</strong>
						
						For eligibility the candidates must:
						<ul class="list-immigration">
							<li>Age between 22 and 55 years;</li>
							<li>Sufficiency in funds to establish the business and settle along with supporting all dependent family members for up to two years;</li>
							<li>Yearly business trip to New Brunswick and present business scenario;</li>
							<li>Ability to submit a refundable deposit of $75,000 CAD and a signed copy of the Deposit Agreement prior to nomination (conditional);</li>
							<li>Have a high school diploma;</li>
							<li>Proves proficiency over English and, or French language so as to manage business flawlessly in New Brunswick;</li>
							<li>Should have willingness to settle and engage business in New Brunswick;</li>
							<li>Present a proof of management experience in three of the last five years;</li>
							<li>Demographic presentation of a business plan, approved by a New Brunswick government;</li>
							<li>Financial stability of at least $300,000 CAD;</li>
							<li>Interact with the NBPNP official for an interview related to the business trip; and</li>
							<li>Score minimum 50 points on the assessment.</li>
						</ul>
						
								 
