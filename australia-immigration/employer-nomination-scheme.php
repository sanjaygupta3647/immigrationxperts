                            <h3 class="txt mt-4">Eligibility Criteria</h3>
                            <p class="about-one__text-2"><span>1. Check for an eligible occupation</span></p>
                            <p class="about-one__text-2">Your occupation should be listed in the relevant skilled
                                occupations.</p>
                            <p class="about-one__text-2"><span>2. Work experience</span></p>
                            <p class="about-one__text-2">For those candidates with their skills assessment requirements
                                approved, should have a minimum of 3 years applicable work experience in their nominated
                                occupation. </p>
                            <p class="about-one__text-2"><span>3. Licensing, registration and membership</span></p>
                            <p class="about-one__text-2">You should be a licensed, registered or a member of a
                                professional body if its compulsory in the state or territory in which you would like to
                                work.</p>
                            <p class="about-one__text-2"><span>4. Completed and approved skills assessment</span></p>
                            <p class="about-one__text-2">The applicant needs to submit skills assessment that directly
                                implies that they have requisite skills to work under the nominated position. </p>
                            <p class="about-one__text-2"><span>5. Be nominated</span></p>
                            <p class="about-one__text-2">It is necessary for you to be nominated by an Australian
                                employer who is operating his business actively and lawfully.<br>
                                You should apply for the visa within a period of 6 months after your nomination
                                approved.</p>
                            <h3 class="txt mt-4">Nomination by an Employer</h3>
                            <p class="about-one__text-2">Nomination could be defined as the process of recognizing a
                                position that is needed to be filled through an overseas skilled worker in a particular
                                business. Depending on the type of visa, stream and also the required occupation, the
                                employer must be approved as a sponsor or they must require to have negotiated a labour
                                agreement with the Department.</p>
                            <p class="about-one__text-2">Employer that use the labour agreement can only nominate
                                foreign workers under occupations that are specified in the labour agreement provided by
                                them. Skilled Occupation Lists is however used for other types of visa and streams that
                                further limits the type of occupation that can be included in the nomination.</p>
                            <p class="about-one__text-2"><span>1. Age criteria</span></p>
                            <p class="about-one__text-2">You should be under 45 years of age to become eligible to apply
                                for this visa type.</p>
                            <p class="about-one__text-2"><span>2. Meet the health requirements</span></p>
                            <p class="about-one__text-2">You and other family members who are applying for the visa
                                should meet all health requirements. The authorities can also ask to submit health
                                information for family members irrespective of them immigrating to Australia along with
                                the main applicant.</p>
                            <p class="about-one__text-2"><span>3. Meet the character requirement</span></p>
                            <p class="about-one__text-2">You and your family members, those are aged 16 years and above
                                and are applying for this visa should meet the character requirements defined by the
                                Australian government. However, the authorities can also ask to submit the character
                                information for those family members that are not immigrating to Australia along with
                                the main applicant.</p>
