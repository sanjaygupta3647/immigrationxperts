               <h3 class="txt mt-4">Sweden Job Seeker Visa Eligibility Requirements</h3>
                
                <ul class="my-3">
                    <li><strong>Valid Passport:</strong> You must hold a valid passport for the duration of your stay.</li>
                    <li><strong>Proof of Funds:</strong> You need to show that you can financially support yourself during your stay in Sweden.</li>
                    <li><strong>Qualifications:</strong> You should have a higher education qualification, typically a degree or diploma in your field.</li>
                    <li><strong>Health Insurance:</strong> You must have valid health insurance that covers your stay in Sweden.</li>
                    <li><strong>Clean Record:</strong> You must not have any criminal history or immigration violations.</li>
                    <li><strong>Non-EU Resident:</strong> To apply for the Sweden Work Visa, you must be a non-EU resident.</li>
                </ul>
                
