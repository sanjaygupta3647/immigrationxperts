                                29, 2019, may only invest in shares of companies with UK registrations.</p>
                            <p class="about-one__text-2">After receiving your visa, you have three months to go to the
                                UK, transfer money to a local investing account, and purchase shares in the firms of
                                your choice.</p>
                            <p class="about-one__text-2">The necessary bare minimum is £2,000,000. The greater it is,
