                            <p class="about-one__text-2">The process of preparing your application takes 1-2 weeks. We
                                then submit it together with all accompanying documentation. To get your fingerprints
                                and photo taken, all you have to do is visit a visa centre. In either 8 weeks (for
                                regular service) or 5 working days (for expedited service), a decision will be made.</p>
                            <h5 class="txt mt-4">Requesting ILR</h5>
                            <p class="about-one__text-2">You can apply for ILR in the United Kingdom after 2, 3, or 5
                                years of nonstop residence here, depending on your investment (see the chart above). All
                                of your family members, including your spouse, any children who are still minors, and
                                any children who turned 18 since you first applied, can settle with you at the same time
                                as long as they continue to depend on you financially.</p>
                            <h5 class="txt mt-4">Exceptions</h5>
                            <p class="about-one__text-2"><span>➤</span> If you made an investment before 29 March 2019
                                and have government bonds in your portfolio, you must file a claim for settlement by 6
                                April 2025.</p>
                            <p class="about-one__text-2"><span>➤</span> You must submit an application for ILR before 6
                                April 2022 if you invested £1,000,000 before 6 November 2014 and have previously
                                extended it a few times under the previous regulations. If for whatever reason you are
                                unable to do so, you may still submit an application to settle, but only if you expand
                                your investment portfolio to $2,000,000. Please keep in mind that a holding period of
                                (2, 3 or 5 years) will begin anew in this scenario.</p>
                            <h5 class="txt mt-4">ILR application requirements</h5>
                            <p class="about-one__text-2">To apply for Indefinite Leave to Remain (ILR), you must
                                demonstrate the following:</p>
                            <p class="about-one__text-2"><span>➤</span> Continuous presence in the UK: You need to
                                provide evidence that you have remained in the UK without any breaks exceeding 180 days
                                within a 12-month period since your initial entry.</p>
                            <p class="about-one__text-2"><span>➤</span> Financial stability: You should not have reduced
                                your portfolio by withdrawing funds.</p>
                            <p class="about-one__text-2"><span>➤</span> English language proficiency: Both you and any
                                adult dependants must have passed an English test at Level B1 or higher.</p>
                            <p class="about-one__text-2"><span>➤</span> Knowledge of life in the UK: You and all adult
                                dependants must have successfully passed the Life in the UK test.</p>
                            <p class="about-one__text-2"><span>➤</span> Rights and privileges: Acquiring ILR grants you
                                nearly the same rights as British citizens. Once you obtain settled status, you have the
                                option to reclaim any funds you had provided as part of the application. </p>
                            <p class="about-one__text-2"><span>➤</span> Additionally, after one year, you can apply for
                                naturalisation in the United Kingdom, which would allow you to obtain your first British
                                passport.</p>
