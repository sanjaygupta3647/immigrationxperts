                            <p class="about-one__text-2">The Tier 1 Investor Visa route has been closed as of February
                                17, 2022. Both new applications and moving from another category are not permitted.
                                Prior to that time, candidates may still apply for permanent residency (ILR), extend
                                their visas, and bring family members.</p>
                            <h5 class="txt mt-4">The benefits of a UK investment visa</h5>
                            <p class="about-one__text-2">With its robust economy, entrepreneurism, and high standard of
                                life, Great Britain has long been a popular destination for businesspeople from across
                                the globe. Many foreigners travel to the UK every year for job, study, and to launch new
                                businesses.</p>
                            <p class="about-one__text-2">Most of the time, in order to become a resident of the UK, you
                                must demonstrate your education and experience, speak at least basic English, and fulfil
                                other strict requirements set down by the UK immigration authorities. The fortunate
                                exception to this rule is those who are applying for a Tier 1 Investor Visa.</p>
                            <h5 class="txt mt-4">What is a Tier 1 Investor visa?</h5>
                            <p class="about-one__text-2">The UK Tier 1 investor visa is a visa designed for wealthy
                                individuals from other countries who wish to relocate to the United Kingdom. By
                                investing a minimum of £2 million, applicants can apply for indefinite leave to remain
                                (ILR) more quickly if they choose to invest a larger amount. The earliest time frame for
                                settling in the UK is two years, and it takes approximately five years to become a
                                British citizen.</p>
                            <p class="about-one__text-2">To be eligible for a Tier 1 Investor visa, individuals must
                                invest their money in a company or companies registered in the UK, pass due diligence
                                checks, and establish an investment account. In return for their substantial investment,
                                visa holders enjoy extensive freedom to pursue various activities within the UK. The
                                initial visa is issued for a period of three years and four months, and if the applicant
                                is not yet eligible to apply for ILR at that point, it can be extended for an additional
                                two years.</p>
                            <p class="about-one__text-2">The UK investor visa offers numerous advantages, which we will
                                now discuss.</p>
                            <h5 class="txt mt-4">Here are seven advantages of the UK investor visa:</h5>
                            <p class="about-one__text-2"><span>➤</span> Swift Application Process: The UK investor visa
                                offers one of the quickest application processes among UK visas. From submitting the
                                application to receiving the visa, the entire procedure takes no longer than three
                                months. Typically, the Home Office makes a decision within 15 days. By paying an
                                additional £500 for a priority service, you can expedite the process even further.
                            </p>
                            <p class="about-one__text-2"><span>➤</span> Inclusion of Family Members: Your spouse,
                                unmarried partner, and minor children can be included in your UK investor visa
                                application, allowing them to benefit from the visa along with you.</p>
                            <p class="about-one__text-2"><span>➤</span> No Work Experience, Education, or English Test
                                Required: Unlike many other visas, the UK investor visa does not necessitate
                                demonstrating work experience, providing proof of education, or taking an English
                                proficiency test.
                            </p>
                            <p class="about-one__text-2"><span>➤</span> Versatile Lifestyle: With the UK investor visa,
                                you and your family have the freedom to work, conduct business, study, or simply enjoy
                                the comforts and safety of everyday life in a modern European country. Your investment
                                grants you access to high standards of healthcare, education, and security.</p>
                            <p class="about-one__text-2"><span>➤</span> Profitable Investment Opportunities: Investing
                                in shares of UK companies can be financially rewarding. As the investor, you have the
                                autonomy to choose where to invest, remaining in control. When your visa expires or you
                                obtain indefinite leave to remain, you can sell the shares and recoup your investment
                                with potential gains.</p>
                            <p class="about-one__text-2"><span>➤</span> Visa Duration and Extensions: The UK investor
                                visa is valid for three years and four months if applied for outside the UK, and three
                                years if applied for within the country. It can be extended for an additional two years,
                                or you may apply for settlement immediately, depending on the amount you have invested.
                            </p>
                            <p class="about-one__text-2"><span>➤</span> Path to British Citizenship: If you invest more
                                than £5,000,000, you can apply for British citizenship after residing in Great Britain
                                for five years. This makes the UK investor visa one of the swiftest routes to obtaining
                                a UK passport.</p>
                            <h5 class="txt mt-4">What is prohibited when on an investment visa in the UK</h5>
                            <p class="about-one__text-2">In the UK, there are just a few restrictions on investors'
                                professional activities:</p>
                            <p class="about-one__text-2"><span>➤</span> Without the necessary training and/or
                                experience, they cannot practise medicine or dentistry in the UK.</p>
                            <p class="about-one__text-2"><span>➤</span> They are unable to make a living as professional
                                athletes or coaches since those occupations need a different type of visa.</p>
                            <p class="about-one__text-2"><span>➤</span> No matter what their employment, individuals are
                                not eligible to claim for social assistance.</p>
                            <h5 class="txt mt-4">Summary of Requirements for Tier 1 Investor Visa:</h5>
                            <p class="about-one__text-2"><span>➤</span> Age Requirement: Applicants must be over 18
                                years old.</p>
                            <p class="about-one__text-2"><span>➤</span> Financial Capacity: Access to £2 to £10 million
                                or more.</p>
                            <p class="about-one__text-2"><span>➤</span> Bank Account: Funds must be held in a foreign or
                                UK bank account that can be easily transferred to the UK.</p>
                            <p class="about-one__text-2"><span>➤</span> Proof of Legitimate Funds: If the funds have
                                been held for less than 2 years, evidence of legal sourcing, such as inheritance or
                                business dividends, must be provided.</p>
                            <p class="about-one__text-2"><span>➤</span> Spousal/Partner Funds: If using funds from a
                                spouse or partner, a marriage certificate or evidence of living together is required,
                                along with a sponsor's letter granting access to the funds.
                            </p>
                            <p class="about-one__text-2"><span>➤</span> Investment Account: Opening an investment
                                account in a regulated UK financial institution is mandatory, and a letter from an
                                authorised bank official must be obtained.</p>
                            <p class="about-one__text-2"><span>➤</span> Background Checks: UK banks conduct background
                                checks on all potential clients for the applicant's benefit.</p>
                            <p class="about-one__text-2"><span>➤</span> Criminal Record Certificate: A criminal record
                                certificate covering the past 10 years and any country where the applicant resided for
                                more than 12 months must be provided.</p>
                            <p class="about-one__text-2"><span>➤</span> Tuberculosis Test: Applicants from listed
                                countries may be required to undergo a tuberculosis test for their Tier 1 Investor visa
                                application.</p>
