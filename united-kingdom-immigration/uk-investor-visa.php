                                at least £5,000,000 is made, an application for a UK passport can be made after residing
                                in the country for 5 years. If the investment is less than £2,000,000, an application
                                for British citizenship can only be made after continuously residing in the British
                                Isles for 6 years. However, regardless of the investment amount, naturalisation is only
                                possible one year after settling in the UK.</p>
