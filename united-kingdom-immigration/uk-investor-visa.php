                                requirements:</h5>
                            <p class="about-one__text-2"><span>➤</span> Within 90 days of obtaining your visa, you
                                arrived in the UK and purchased shares of UK companies worth at least £2 million.
                                Additionally, both you and any adult dependants accompanying you registered with the
                                police within seven days of arrival.</p>
                            <p class="about-one__text-2"><span>➤</span> Since entering the UK, you have not withdrawn
                                any funds from your investment portfolio. It is worth noting that if the value of your
                                shares has fallen below £2,000,000 during this period, you are not required to top it up
                                in order to extend your visa.</p>
                            <p class="about-one__text-2"><span>➤</span> Your spouse or partner, minor children, and any
                                children who have turned 18 since arriving in the UK on your initial investor visa can
                                apply to extend their visas alongside you.</p>
                            <p class="about-one__text-2"><span>➤</span> If you submitted your application before March
                                29, 2019, and invested in government bonds, you are eligible to extend your UK investor
                                visa.</p>
                            <p class="about-one__text-2"><span>➤</span> If you initially applied under the previous
